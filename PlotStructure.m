function PlotStructure(A0toj,OF,cc)
    figure(cc);
    hold on;
    plot([A0toj(1,4,1,2),A0toj(1,4,2,2)],[A0toj(2,4,1,2),A0toj(2,4,2,2)],'-ko','linewidth',2,...
        'markersize',5);
    plot([A0toj(1,4,2,2),A0toj(1,4,3,6)],[A0toj(2,4,2,2),A0toj(2,4,3,6)],'-ko','linewidth',2,...
        'markersize',5);
    plot([A0toj(1,4,1,4),A0toj(1,4,2,4)],[A0toj(2,4,1,4),A0toj(2,4,2,4)],'-ko','linewidth',2,...
        'markersize',5);
    plot([A0toj(1,4,2,4),A0toj(1,4,3,6)],[A0toj(2,4,2,4),A0toj(2,4,3,6)],'-ko','linewidth',2,...
        'markersize',5);
    plot([A0toj(1,4,1,5),A0toj(1,4,3,6)],[A0toj(2,4,1,5),A0toj(2,4,3,6)],'-ko','linewidth',2,...
        'markersize',5);
    plot([A0toj(1,4,3,6),A0toj(1,:,1,7)*[OF;0;0;1]],[A0toj(2,4,3,6),A0toj(2,:,1,7)*[OF;0;0;1]],...
        '-ko','linewidth',2,'markersize',5);
    plot([A0toj(1,4,1,7),A0toj(1,:,1,7)*[OF;0;0;1]],[A0toj(2,4,1,7),A0toj(2,:,1,7)*[OF;0;0;1]],...
        '-ko','linewidth',2,'markersize',5); 
end

